/* ============================================================
   main.c — ЭТО ТВОЙ ФАЙЛ.
   Слой 1: мигаем встроенным светодиодом LD2 (он на ножке PA5).
   Остальные файлы — обвязка, в них пока лезть не нужно.

   Логика простая: включить порт -> сделать ножку выходом ->
   в бесконечном цикле переключать её и ждать.
   ============================================================ */

#include <stdint.h>
#include "stm32f411.h"

/* Грубая задержка: просто жжём такты в пустом цикле.
   volatile нужен, чтобы компилятор не удалил "ничего не делающий" цикл.
   Точность так себе (чип после включения работает на 16 МГц) —
   на слое 5 заменим это настоящим таймером SysTick.                 */
static void delay(volatile uint32_t count)
{
    while (count--) { }
}

int main(void)
{
    /* 1. Подаём тактирование на порт A.
          Без включённого такта периферия молчит — это САМАЯ
          частая первая ошибка на STM32.                          */
    RCC_AHB1ENR |= RCC_AHB1ENR_GPIOAEN;

    /* 2. Делаем PA5 цифровым выходом.
          Каждой ножке в MODER отведены 2 бита; у ножки 5 это
          биты 11:10. Значение 01 = "выход".                      */
    GPIOA_MODER &= ~(3U << 10);   /* очистить оба бита ножки 5 */
    GPIOA_MODER |=  (1U << 10);   /* 01 = выход                */

    /* 3. Мигаем вечно: переключили ножку -> подождали -> повторили. */
    while (1) {
        GPIOA_ODR ^= (1U << 5);   /* инвертировать PA5 -> диод меняет состояние */
        delay(800000);            /* пауза, чтобы глаз успел заметить            */
    }
}

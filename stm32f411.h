/* ============================================================
   stm32f411.h — имена нужных нам регистров.
   Это наша крошечная замена официальным заголовкам CMSIS:
   позже, когда регистров станет много, подключим настоящие
   от ST, а пока полезно увидеть адреса своими глазами.

   Все адреса — из reference manual чипа (RM0383).
   Каждый регистр 32-битный. volatile = "эта память может
   меняться сама" (это железо), поэтому компилятор не имеет
   права её кэшировать или выкидывать обращения к ней.
   ============================================================ */

#ifndef STM32F411_H
#define STM32F411_H

#include <stdint.h>

/* RCC — включает/выключает тактирование периферии (RM0383 §6.3) */
#define RCC_AHB1ENR  (*(volatile uint32_t *)0x40023830U)  /* тактирование портов GPIO */
#define RCC_AHB1ENR_GPIOAEN  (1U << 0)                     /* бит 0: включить порт A   */

/* GPIOA, база 0x40020000 (RM0383 §8.4) */
#define GPIOA_MODER  (*(volatile uint32_t *)0x40020000U)  /* режим вывода: вход/выход/... */
#define GPIOA_ODR    (*(volatile uint32_t *)0x40020014U)  /* состояние выходов порта      */

#endif /* STM32F411_H */
